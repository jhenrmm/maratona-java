package academy.devdojo.maratonajava.introdução;

public class Aula06 {
    public static void main(String[] args) {
        // while, so while, for
        int count = 10;
        while (count<10){
            System.out.println("Olá");
            count++;

        }
        do {
            System.out.println("Mundo");
        }while (count<10);
        for (int i=0;i<10;i++){
            System.out.println("For");
        }
    }
}
